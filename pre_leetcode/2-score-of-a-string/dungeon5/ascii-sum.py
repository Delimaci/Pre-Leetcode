# # Dungeon 5 — ASCII Sum Challenge ⚔️
# **Goal:** Get comfortable summing ASCII values of all characters in a string.

# ## Tasks:
# 1. Ask the user to enter a string.
# 2. Initialize a variable `ascii_sum` to 0.
# 3. Loop through **each character** in the string.
# 4. For each character:
#    - Use `ord()` to get its ASCII value.
#    - Add the ASCII value to `ascii_sum`.
# 5. After the loop, print the total ASCII sum.

# **Purpose:** Prepares you to think about totals and summing values, which is similar to accumulating differences in the Score of a String problem.



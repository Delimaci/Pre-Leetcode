# # Dungeon 6 — ASCII Average Calculator ⚔️
# **Goal:** Calculate the average ASCII value of all characters in a string.

# ## Tasks:
# 1. Ask the user to enter a string.
# 2. Initialize a variable `ascii_sum` to 0.
# 3. Loop through **each character** in the string:
#    - Use `ord()` to get its ASCII value.
#    - Add the value to `ascii_sum`.
# 4. After the loop, calculate the **average** by dividing `ascii_sum` by the length of the string.
# 5. Print the average ASCII value.

# **Purpose:** Builds on summing ASCII values, and introduces calculating averages to analyze strings numerically.


